<template>
    <div class="classify-section" v-show="arrClassify.length">
        <div class="classify-indicator iconfont icon-biaoqian" :style="{ top: numCurrent * 30 + 'px' }"></div>
        <div class="classify-item"
             @click="handleSelect(item)"
             :class="[numCurrent === index && 'active']"
             v-for="(item, index) in arrClassify"
             :key="index">
            <span>{{item.title}} ({{item.number}})</span>
        </div>
       <div class="copyright">
           <a
               href="https://beian.miit.gov.cn/"
               rel="noreferrer"
               target="_blank"
           >
               湘ICP备2022023602号-1
           </a>
           <p>Copyright © {{ new Date().getFullYear() }} daysnap.cn</p>
       </div>
    </div>
</template>

<script>
    export default {
        data () {
            return {
                numCurrent: 0,
                arrClassify: [],
            }
        },
        watch: {
            '$route.params' (v) {
                this.assignmentData(v);
            },
        },
        created () {
            this.reqClassifyList();
        },
        methods: {
            reqClassifyList () {
                this.$curl.get('mocks/classify.json').then((res) => {
                    let number = 0;
                    res.forEach((item) => number += item.number);
                    this.arrClassify = [{ title: '全部', number, }, ...res];
                    this.assignmentData();
                });
            },
            handleSelect (item) {
                let path = item.title === '全部' ? '/' : `/classify/${item.title.toLocaleLowerCase()}`;
                if (this.$route.path !== path) {
                    document.documentElement.scrollTop = 0;
                    document.body.scrollTop = 0;
                }
                this.$router.push({ path, query: this.$route.query });
            },
            assignmentData (v = this.$route.params) {
                let index = 0;
                this.arrClassify.forEach((item, i) => {
                    if (v.classify === item.title.toLocaleLowerCase()) index = i;
                });
                this.numCurrent = index;
            }
        },
    }
</script>

